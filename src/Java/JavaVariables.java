package Java;

public class JavaVariables {
    public static void main(String[] args){
        //primitive data types
        //int - number
        int height = 180;
        System.out.println(height);
        //double, stores bigger whole numbers and decimal numbers.
        double weight = 68.5;
        System.out.println(weight);
        //boolean - true or false
        boolean isNikolinaTall = true;
        System.out.println(isNikolinaTall);
        //char can hold any data type letter space punctuation mark surrounded with '':
        char nikolinasBloodType = '+';
        System.out.println(nikolinasBloodType);
        
    }
}
